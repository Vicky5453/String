package muthu;

public class Pattern1_5 {

	public static void main(String[] args) {
		   for(int s=5;s>=1;s--)
		      {
		    	 
		    	  for(int i=s-1;i>=1;i--)
		    	  {
		    		  System.out.print(" "+ " ");
		    	  }
		    	  for(int k=1;k<=6-s;k++)
		    	  {
		    		  System.out.print("* ");
		    	  }
		    	  System.out.println();
		      } 
		 

	}

}
